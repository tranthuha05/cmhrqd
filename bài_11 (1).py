print("""
============================================================
4. CÂU 11.3.2 — HUẤN LUYỆN Q-LEARNING
============================================================
""")

env = VietnamEconomyEnv()

Q = np.zeros((3, 3, 3, 3, 5))

alpha = 0.10
gamma_discount = 0.95
n_episodes = 10000

episode_rewards = []
epsilon_history = []

for ep in range(n_episodes):
    s, _ = env.reset()
    total_reward = 0

    epsilon = max(0.05, 1.0 - ep / 5000)
    epsilon_history.append(epsilon)

    while True:
        if np.random.rand() < epsilon:
            a = env.action_space.sample()
        else:
            a = int(np.argmax(Q[tuple(s)]))

        s2, r, done, truncated, info = env.step(a)

        old_value = Q[tuple(s) + (a,)]
        next_max = Q[tuple(s2)].max()

        Q[tuple(s) + (a,)] = old_value + alpha * (
            r + gamma_discount * next_max - old_value
        )

        s = s2
        total_reward += r

        if done or truncated:
            break

    episode_rewards.append(total_reward)

print("Training hoàn tất.")
print("Reward trung bình 100 episode cuối:", np.mean(episode_rewards[-100:]))

# Learning curve
reward_series = pd.Series(episode_rewards)
rolling_reward = reward_series.rolling(window=100).mean()

plt.figure(figsize=(12,6))
plt.plot(episode_rewards, alpha=0.25, label="Episode reward")
plt.plot(rolling_reward, linewidth=3, label="Rolling mean 100 episodes")
plt.title("Learning curve của Q-learning")
plt.xlabel("Episode")
plt.ylabel("Total reward")
plt.legend()
plt.grid(True)
plt.show()

plt.figure(figsize=(10,5))
plt.plot(epsilon_history)
plt.title("Epsilon decay")
plt.xlabel("Episode")
plt.ylabel("Epsilon")
plt.grid(True)
plt.show()

