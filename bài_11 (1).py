action_names = {
    0: "a0 - Truyền thống",
    1: "a1 - Cân bằng",
    2: "a2 - Số hóa nhanh",
    3: "a3 - AI dẫn dắt",
    4: "a4 - Bao trùm"
}

state_level_names = {
    0: "low",
    1: "medium",
    2: "high"
}

class VietnamEconomyEnv(gym.Env):
    def __init__(self):
        super().__init__()

        self.action_space = spaces.Discrete(5)
        self.observation_space = spaces.MultiDiscrete([3, 3, 3, 3])

        self.T = 10
        self.budget = 1000

        self.allocation = {
            0: np.array([0.70, 0.10, 0.10, 0.10]),
            1: np.array([0.40, 0.25, 0.15, 0.20]),
            2: np.array([0.25, 0.45, 0.15, 0.15]),
            3: np.array([0.20, 0.20, 0.45, 0.15]),
            4: np.array([0.30, 0.20, 0.10, 0.40])
        }

        self.w = np.array([0.40, 0.25, 0.20, 0.15])

        self.alpha_K = 0.33
        self.alpha_L = 0.42
        self.alpha_D = 0.10
        self.alpha_AI = 0.08
        self.alpha_H = 0.07

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)

        self.t = 0

        # Trạng thái VN 2026 giả định:
        # GDP medium, Digital medium, AI low, Unemployment medium
        self.state = np.array([1, 1, 0, 1])

        self.K = 27500.0
        self.D = 20.3
        self.AI = 86.0
        self.H = 30.0
        self.L = 54.0
        self.A = 35.0

        self.prev_Y = self.production()

        return self.state.copy(), {}

    def production(self):
        Y = (
            self.A
            * (self.K ** self.alpha_K)
            * (self.L ** self.alpha_L)
            * (self.D ** self.alpha_D)
            * (self.AI ** self.alpha_AI)
            * (self.H ** self.alpha_H)
        )
        return Y

    def discretize_state(self, gdp_growth, digital_index, ai_capacity, unemployment_risk):
        if gdp_growth < 0.03:
            gdp_state = 0
        elif gdp_growth < 0.06:
            gdp_state = 1
        else:
            gdp_state = 2

        if digital_index < 35:
            d_state = 0
        elif digital_index < 70:
            d_state = 1
        else:
            d_state = 2

        if ai_capacity < 100:
            ai_state = 0
        elif ai_capacity < 180:
            ai_state = 1
        else:
            ai_state = 2

        if unemployment_risk < 0.04:
            u_state = 0
        elif unemployment_risk < 0.08:
            u_state = 1
        else:
            u_state = 2

        return np.array([gdp_state, d_state, ai_state, u_state])

    def step(self, action):
        allocation = self.allocation[action]

        invest_K = allocation[0] * self.budget
        invest_D = allocation[1] * self.budget
        invest_AI = allocation[2] * self.budget
        invest_H = allocation[3] * self.budget

        # Cập nhật trạng thái kinh tế
        self.K = 0.95 * self.K + invest_K
        self.D = 0.88 * self.D + invest_D / 100
        self.AI = 0.85 * self.AI + invest_AI / 20
        self.H = 0.98 * self.H + invest_H / 200

        # TFP tăng nhờ D, AI, H
        self.A = self.A * (1 + 0.0008*self.D + 0.0005*self.AI + 0.001*self.H)

        Y = self.production()
        gdp_growth = (Y - self.prev_Y) / self.prev_Y

        # Rủi ro thất nghiệp tăng khi AI cao hơn H, giảm khi H cao
        unemployment_risk = max(
            0.01,
            0.08
            + 0.0008 * invest_AI
            - 0.0012 * invest_H
            - 0.002 * self.H
        )

        # Cyber risk tăng theo AI, giảm theo H
        cyber_risk = max(
            0,
            0.03
            + 0.0006 * invest_AI
            - 0.0004 * invest_H
        )

        # Emission tăng theo K và AI, giảm nhẹ khi D/H tốt hơn
        emission = max(
            0,
            0.04
            + 0.0004 * invest_K
            + 0.0005 * invest_AI
            - 0.0002 * invest_D
        )

        # Chuẩn hóa reward để tránh quá lớn
        delta_gdp_score = gdp_growth * 100
        unemployment_score = unemployment_risk * 100
        cyber_score = cyber_risk * 100
        emission_score = emission * 100

        reward = (
            self.w[0] * delta_gdp_score
            - self.w[1] * unemployment_score
            - self.w[2] * cyber_score
            - self.w[3] * emission_score
        )

        self.state = self.discretize_state(
            gdp_growth=gdp_growth,
            digital_index=self.D,
            ai_capacity=self.AI,
            unemployment_risk=unemployment_risk
        )

        self.prev_Y = Y
        self.t += 1

        terminated = self.t >= self.T
        truncated = False

        info = {
            "Y": Y,
            "gdp_growth": gdp_growth,
            "D": self.D,
            "AI": self.AI,
            "H": self.H,
            "unemployment_risk": unemployment_risk,
            "cyber_risk": cyber_risk,
            "emission": emission,
            "reward": reward
        }

        return self.state.copy(), reward, terminated, truncated, info


