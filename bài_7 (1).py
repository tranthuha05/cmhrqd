class VietnamDigitalProblem(ElementwiseProblem):
    def __init__(self):
        super().__init__(
            n_var=24,
            n_obj=4,
            n_ieq_constr=14,
            xl=np.zeros(24),
            xu=np.ones(24) * 12000
        )

        self.beta = beta
        self.e = e
        self.rho = rho
        self.sigma = sigma

    def _evaluate(self, x, out, *args, **kwargs):
        X = x.reshape(6, 4)

        # Tổng ngân sách theo vùng
        region_sum = X.sum(axis=1)

        # f1: maximize GDP gain => minimize negative GDP gain
        gdp_gain = (self.beta * X).sum()
        f1 = -gdp_gain

        # f2: minimize inequality, dùng mean absolute deviation
        f2 = np.abs(region_sum - region_sum.mean()).mean()

        # f3: minimize emissions from infrastructure and AI
        f3 = (self.e * (X[:, 0] + X[:, 2])).sum()

        # f4: minimize net data security risk
        f4 = (self.rho * X[:, 2]).sum() - (self.sigma * X[:, 3]).sum()

        out["F"] = [f1, f2, f3, f4]

        # Ràng buộc dạng G <= 0
        G = []

        # C1 total budget <= 50000
        G.append(X.sum() - 50000)

        # C2 each region >= 5000 => 5000 - region_sum <= 0
        for i in range(6):
            G.append(5000 - region_sum[i])

        # C3 each region <= 12000
        for i in range(6):
            G.append(region_sum[i] - 12000)

        # C4 human capital >= 12000
        G.append(12000 - X[:, 3].sum())

        out["G"] = np.array(G)

