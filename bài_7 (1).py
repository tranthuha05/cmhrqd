# TOPSIS trên tập Pareto
# Các tiêu chí:
# GDP_gain là benefit
# Inequality, Emission, Data_risk là cost

policy_weights = np.array([0.40, 0.25, 0.20, 0.15])
is_benefit = np.array([True, False, False, False])

Y = pareto_df[["GDP_gain", "Inequality_MAD", "Emission", "Data_risk"]].values.astype(float)

# Vector normalization
R = Y / np.sqrt((Y ** 2).sum(axis=0))

V = R * policy_weights

A_star = np.where(is_benefit, V.max(axis=0), V.min(axis=0))
A_neg = np.where(is_benefit, V.min(axis=0), V.max(axis=0))

S_star = np.sqrt(((V - A_star) ** 2).sum(axis=1))
S_neg = np.sqrt(((V - A_neg) ** 2).sum(axis=1))

C_star = S_neg / (S_star + S_neg)

pareto_df["TOPSIS_score"] = C_star
pareto_df["Solution_ID"] = np.arange(len(pareto_df))

best_idx = pareto_df["TOPSIS_score"].idxmax()

