def solve_cvxpy(use_fairness=True, lam=0.65):
    beta_matrix = np.array([[beta[(r,j)] for j in items] for r in regions])
    D0_vec = np.array([D0[r] for r in regions])

    X = cp.Variable((6,4), nonneg=True)

    objective = cp.Maximize(cp.sum(cp.multiply(beta_matrix, X)))

    constraints = []

    constraints.append(cp.sum(X) <= total_budget)

    for i in range(6):
        constraints.append(cp.sum(X[i, :]) >= min_region_budget)
        constraints.append(cp.sum(X[i, :]) <= max_region_budget)

    constraints.append(cp.sum(X[:, 3]) >= min_human_budget)

    if use_fairness:
        M = cp.Variable(nonneg=True)

        for i in range(6):
            constraints.append(D0_vec[i] + gamma * X[i, 1] <= M)

        for i in range(6):
            constraints.append(D0_vec[i] + gamma * X[i, 1] >= lam * M)

    prob = cp.Problem(objective, constraints)

    try:
        prob.solve(solver=cp.CLARABEL)
    except:
        prob.solve(solver=cp.SCS)

    if prob.status not in ["optimal", "optimal_inaccurate"]:
        return None, None, prob.status

    result = pd.DataFrame(
        X.value,
        index=[region_names[r] for r in regions],
        columns=[item_names[j] for j in items]
    )

    return result, prob.value, prob.status

