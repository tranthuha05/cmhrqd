def solve_pulp(use_fairness=True, lam=0.65, max_region_cap=12000):
    model = pulp.LpProblem("VN_Digital_Budget", pulp.LpMaximize)

    x = pulp.LpVariable.dicts("x", (regions, items), lowBound=0)

    # Hàm mục tiêu
    model += pulp.lpSum(beta[(r,j)] * x[r][j] for r in regions for j in items), "GDP_gain"

    # C1
    model += pulp.lpSum(x[r][j] for r in regions for j in items) <= total_budget, "Budget_total"

    # C2, C3
    for r in regions:
        model += pulp.lpSum(x[r][j] for j in items) >= min_region_budget, f"Min_region_{r}"
        model += pulp.lpSum(x[r][j] for j in items) <= max_region_cap, f"Max_region_{r}"

    # C4
    model += pulp.lpSum(x[r]["H"] for r in regions) >= min_human_budget, "Min_human_capital"

    # C5
    if use_fairness:
        M = pulp.LpVariable("Dmax", lowBound=0)

        for r in regions:
            model += D0[r] + gamma * x[r]["D"] <= M, f"Dmax_upper_{r}"

        for r in regions:
            model += D0[r] + gamma * x[r]["D"] >= lam * M, f"Fairness_lower_{r}"

    model.solve(pulp.PULP_CBC_CMD(msg=False))

    status = pulp.LpStatus[model.status]

    if status != "Optimal":
        return None, None, status, model

    result = pd.DataFrame(
        index=[region_names[r] for r in regions],
        columns=[item_names[j] for j in items]
    )

    for r in regions:
        for j in items:
            result.loc[region_names[r], item_names[j]] = x[r][j].value()

    result = result.astype(float)
    z_value = pulp.value(model.objective)

    return result, z_value, status, model

