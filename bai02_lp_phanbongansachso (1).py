# Tối đa hóa Z = - tối thiểu hóa (-Z)
c = [-0.85, -1.20, -0.95, -1.35]

# Ràng buộc dạng A_ub x <= b_ub
A_ub = [
    [1, 1, 1, 1],             # tổng ngân sách <= 100
    [-1, 0, 0, 0],            # x1 >= 25  -> -x1 <= -25
    [0, -1, 0, 0],            # x2 >= 15
    [0, 0, -1, 0],            # x3 >= 20
    [0, 0, 0, -1],            # x4 >= 10
    [0.35, -0.65, 0.35, -0.65] # x2+x4 >= 0.35 total
]

b_ub = [100, -25, -15, -20, -10, 0]

res = linprog(
    c,
    A_ub=A_ub,
    b_ub=b_ub,
    bounds=[(0, None)] * 4,
    method="highs"
)

print("Trạng thái nghiệm:", res.message)

x_scipy = res.x
z_scipy = -res.fun

