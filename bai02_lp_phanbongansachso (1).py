print("""
============================================================
4. CÂU 2.4.2 — GIẢI BẰNG PuLP VÀ GIÁ ĐỐI NGẪU
============================================================

PuLP cho phép đặt tên từng ràng buộc và lấy shadow price thông qua thuộc tính .pi,
nếu solver CBC trả về thông tin đối ngẫu cho bài toán LP.
""")

model = pulp.LpProblem("Digital_Budget_Allocation", pulp.LpMaximize)

x1 = pulp.LpVariable("x1_Ha_tang_so", lowBound=0)
x2 = pulp.LpVariable("x2_AI_du_lieu", lowBound=0)
x3 = pulp.LpVariable("x3_Nhan_luc_so", lowBound=0)
x4 = pulp.LpVariable("x4_RD_cong_nghe", lowBound=0)

# Objective
model += 0.85*x1 + 1.20*x2 + 0.95*x3 + 1.35*x4, "GDP_gain"

# Constraints
model += x1 + x2 + x3 + x4 <= 100, "Ngan_sach_tong"
model += x1 >= 25, "San_ha_tang_so"
model += x2 >= 15, "San_AI_du_lieu"
model += x3 >= 20, "San_nhan_luc_so"
model += x4 >= 10, "San_RD"
model += x2 + x4 >= 0.35*(x1 + x2 + x3 + x4), "Ty_trong_cong_nghe_chien_luoc"

solver = pulp.PULP_CBC_CMD(msg=False)
model.solve(solver)

x_pulp = np.array([x1.value(), x2.value(), x3.value(), x4.value()])
z_pulp = pulp.value(model.objective)

pulp_result_df = pd.DataFrame({
    "Biến": variables,
    "Hạng mục": items,
    "Phân bổ tối ưu": x_pulp,
    "Hệ số GDP": coef,
    "Đóng góp GDP kỳ vọng": coef * x_pulp
})

display(pulp_result_df.round(4))

print(f"Giá trị tối ưu Z* theo PuLP = {z_pulp:.4f} nghìn tỷ VND")

# Shadow price
shadow_rows = []

