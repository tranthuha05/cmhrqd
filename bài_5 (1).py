def project_probability(i):
    if field[i] == "Hạ tầng":
        return 0.85
    elif field[i] == "Chính phủ số":
        return 0.75
    elif field[i] in ["AI", "Bán dẫn"]:
        return 0.65
    else:
        return 0.80

