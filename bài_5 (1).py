def solve_project_selection(
    total_budget=80000,
    early_budget=40000,
    force_p1_p2=False,
    force_p14=True,
    use_expected_value=False
):
    """
    Giải bài toán lựa chọn dự án bằng PuLP.

    total_budget: ngân sách tổng 5 năm
    early_budget: ngân sách năm 1-2
    force_p1_p2: nếu True thì bắt buộc chọn cả P1 và P2
    force_p14: nếu True thì bắt buộc chọn P14
    use_expected_value: nếu True thì tối đa hóa lợi ích kỳ vọng p_i*B_i
    """

    model = pulp.LpProblem("VN_Project_Selection", pulp.LpMaximize)

    y = pulp.LpVariable.dicts("y", projects, cat="Binary")

    if use_expected_value:
        benefit = expected_B
    else:
        benefit = B

    # Objective
    model += pulp.lpSum(benefit[i] * y[i] for i in projects), "Total_Benefit"

    # C1 ngân sách tổng
    model += pulp.lpSum(C[i] * y[i] for i in projects) <= total_budget, "Total_budget"

    # C2 ngân sách năm 1-2
    model += pulp.lpSum(C1[i] * y[i] for i in projects) <= early_budget, "Early_budget"

    # C3 loại trừ trung tâm dữ liệu
    if force_p1_p2:
        model += y[1] == 1, "Force_P1"
        model += y[2] == 1, "Force_P2"
    else:
        model += y[1] + y[2] <= 1, "Data_center_exclusion"

    # C4, C5 tiên quyết
    model += y[8] <= y[12], "AI_requires_training"
    model += y[13] <= y[12], "Semiconductor_requires_training"

    # C6 cân đối lĩnh vực
    model += y[4] + y[5] >= 1, "At_least_one_digital_gov"

    if force_p14:
        model += y[14] >= 1, "Cybersecurity_required"

    # C7 số lượng dự án
    model += pulp.lpSum(y[i] for i in projects) >= 7, "Min_projects"
    model += pulp.lpSum(y[i] for i in projects) <= 11, "Max_projects"

    model.solve(pulp.PULP_CBC_CMD(msg=False))

    status = pulp.LpStatus[model.status]

    if status != "Optimal":
        return None, None, status, model

    selected = [i for i in projects if y[i].value() > 0.5]

    selected_df = project_df[project_df["Mã"].isin([f"P{i}" for i in selected])].copy()

    total_cost = sum(C[i] for i in selected)
    total_early_cost = sum(C1[i] for i in selected)
    total_benefit = sum(B[i] for i in selected)
    total_objective = pulp.value(model.objective)

    summary = {
        "selected": selected,
        "selected_df": selected_df,
        "total_cost": total_cost,
        "total_early_cost": total_early_cost,
        "total_benefit": total_benefit,
        "objective": total_objective,
        "benefit_cost_ratio": total_objective / total_cost if total_cost > 0 else np.nan,
        "num_projects": len(selected)
    }

    return summary, model, status, model

