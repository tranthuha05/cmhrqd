def objective_with_rho(x, rho_value):
    sim = simulate_path(x, shock_2028=False)

    if sim is None:
        return 1e9

    welfare = 0
    for t in range(T):
        if sim["C"][t] <= 0:
            return 1e9
        welfare += (rho_value ** t) * np.log(sim["C"][t])

    return -welfare


