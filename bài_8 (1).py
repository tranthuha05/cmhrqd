def production(A, K, L, D, AI, H):
    """Hàm sản xuất Cobb-Douglas mở rộng."""
    return A * (K ** alpha_K) * (L ** alpha_L) * (D ** alpha_D) * (AI ** alpha_AI) * (H ** alpha_H)


def simulate_path(investment_shares, shock_2028=False):
    """
    investment_shares là vector độ dài 4*T:
    mỗi năm gồm tỷ lệ đầu tư vào K, D, AI, H trên Y_t.
    Tiêu dùng là phần còn lại.

    shares phải không âm và tổng 4 khoản đầu tư mỗi năm < 1.
    """

    shares = investment_shares.reshape(T, 4)

    K = np.zeros(T + 1)
    D = np.zeros(T + 1)
    AI = np.zeros(T + 1)
    H = np.zeros(T + 1)
    A = np.zeros(T + 1)
    Y = np.zeros(T)
    C = np.zeros(T)

    I_K = np.zeros(T)
    I_D = np.zeros(T)
    I_AI = np.zeros(T)
    I_H = np.zeros(T)

    K[0] = K0
    D[0] = D0
    AI[0] = AI0
    H[0] = H0
    A[0] = A0

    welfare = 0

    for t in range(T):
        L_t = L0 * ((1.006) ** t)

        Y_t = production(A[t], K[t], L_t, D[t], AI[t], H[t])

        # Shock năm 2028, tức t = 2
        if shock_2028 and years[t] == 2028:
            Y_t = Y_t * 0.92

        Y[t] = Y_t

        sK, sD, sAI, sH = shares[t]
        total_s = sK + sD + sAI + sH

        if total_s >= 0.95:
            return None

        I_K[t] = sK * Y_t
        I_D[t] = sD * Y_t
        I_AI[t] = sAI * Y_t
        I_H[t] = sH * Y_t

        C[t] = Y_t - I_K[t] - I_D[t] - I_AI[t] - I_H[t]

        if C[t] <= 0:
            return None

        welfare += (rho ** t) * np.log(C[t])

        K[t+1] = (1 - delta_K) * K[t] + I_K[t]
        D[t+1] = (1 - delta_D) * D[t] + I_D[t]
        AI[t+1] = (1 - delta_AI) * AI[t] + I_AI[t]
        H[t+1] = H[t] + theta_H * I_H[t] - mu * H[t]

        A[t+1] = A[t] * (1 + phi1 * D[t] + phi2 * AI[t] + phi3 * H[t])

    result = {
        "K": K,
        "D": D,
        "AI": AI,
        "H": H,
        "A": A,
        "Y": Y,
        "C": C,
        "I_K": I_K,
        "I_D": I_D,
        "I_AI": I_AI,
        "I_H": I_H,
        "welfare": welfare,
        "shares": shares
    }

    return result


def objective(x):
    """Hàm mục tiêu cho scipy: minimize negative welfare."""
    sim = simulate_path(x, shock_2028=False)

    if sim is None:
        return 1e9

    return -sim["welfare"]


def yearly_budget_constraint(x):
    """
    Ràng buộc tổng tỷ lệ đầu tư mỗi năm <= 0.80.
    Để tránh nghiệm tiêu dùng quá thấp, đặt trần đầu tư 80% Y_t.
    """
    shares = x.reshape(T, 4)
    return 0.80 - shares.sum(axis=1)


