def entropy_weights(X_raw):
    """
    Tính trọng số entropy.
    Với tiêu chí chi phí như Gini, đổi hướng trước bằng nghịch đảo min-max.
    """

    X_adj = X_raw.copy().astype(float)

    # Chuyển tiêu chí chi phí thành tiêu chí lợi ích bằng đảo hướng min-max
    for j, benefit in enumerate(is_benefit):
        if not benefit:
            col = X_adj[:, j]
            X_adj[:, j] = col.max() - col + 1e-12

    # Tránh chia 0
    P = X_adj / (X_adj.sum(axis=0) + 1e-12)

    k = 1.0 / np.log(X_adj.shape[0])
    E = -k * np.nansum(P * np.log(P + 1e-12), axis=0)
    d = 1 - E
    w = d / d.sum()

    return w, E, d

