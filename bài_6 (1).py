print("""
============================================================
1. LÝ THUYẾT TOPSIS
============================================================

TOPSIS là phương pháp xếp hạng các phương án dựa trên khoảng cách tới:

- Phương án lý tưởng dương A*: tốt nhất trên tất cả tiêu chí.
- Phương án lý tưởng âm A-: kém nhất trên tất cả tiêu chí.

Quy trình:

Bước 1. Chuẩn hóa vector ma trận quyết định:

r_ij = x_ij / sqrt(sum_i x_ij^2)

Bước 2. Nhân trọng số:

v_ij = w_j * r_ij

Bước 3. Xác định A* và A-:
- Với tiêu chí lợi ích: A* là max, A- là min.
- Với tiêu chí chi phí: A* là min, A- là max.

Bước 4. Tính khoảng cách Euclide:

S_i* = sqrt(sum_j (v_ij - v_j*)^2)
S_i- = sqrt(sum_j (v_ij - v_j-)^2)

Bước 5. Tính hệ số gần gũi:

C_i* = S_i- / (S_i* + S_i-)

C_i* càng lớn thì vùng càng gần phương án lý tưởng và càng được ưu tiên.
""")

